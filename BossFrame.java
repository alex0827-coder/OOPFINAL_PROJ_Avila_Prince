    private void addButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addButtonActionPerformed
  String name = nameField.getText().trim();
    String notes = notesArea.getText().trim();

    if (name.isEmpty()) {
        JOptionPane.showMessageDialog(this, "Boss Name can't be empty!", "Input Error", JOptionPane.WARNING_MESSAGE);
        return;
    }

    java.util.List<String> weaknesses = new java.util.ArrayList<>();
    String phys = physicalCombo.getSelectedItem().toString();
    String elem = elementalCombo.getSelectedItem().toString();
    String status = statusCombo.getSelectedItem().toString();

    if (!phys.equals("None") && !phys.contains("Item")) weaknesses.add(phys);
    if (!elem.equals("None") && !elem.contains("Item")) weaknesses.add(elem);
    if (!status.equals("None") && !status.contains("Item")) weaknesses.add(status);

    String combinedWeakness = String.join(", ", weaknesses);
    String sql = "INSERT INTO bosses (name, weakness, notes) VALUES (?, ?, ?)";
    
    try (Connection conn = DBConnection.getConnection();
         PreparedStatement ps = conn.prepareStatement(sql)) {
        
        ps.setString(1, name);
        ps.setString(2, combinedWeakness);
        ps.setString(3, notes);
        
        ps.executeUpdate();
        JOptionPane.showMessageDialog(this, "Boss configuration added successfully!");
        
        refreshTable(); // <-- MOVED HERE: Refreshes the grid upon successful entry!
        
        // Clear all fields for the next input
        nameField.setText("");
        notesArea.setText("");
        physicalCombo.setSelectedIndex(0);
        elementalCombo.setSelectedIndex(0);
        statusCombo.setSelectedIndex(0);
        
    } catch (SQLException ex) {
        JOptionPane.showMessageDialog(this, "Database write failure: " + ex.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        // Clear name field for retry
        nameField.setText("");
    }
    }//GEN-LAST:event_addButtonActionPerformed

    private void updateButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_updateButtonActionPerformed
    String name = nameField.getText().trim();
    String notes = notesArea.getText().trim();

    if (name.isEmpty() || originalBossName.isEmpty()) {
        JOptionPane.showMessageDialog(this, "Please select a boss from the table first before updating.", "Selection Warning", JOptionPane.WARNING_MESSAGE);
        return;
    }

    // Capture the latest weakness combinations from the dropdown boxes
    java.util.List<String> weaknesses = new java.util.ArrayList<>();
    String phys = physicalCombo.getSelectedItem().toString();
    String elem = elementalCombo.getSelectedItem().toString();
    String status = statusCombo.getSelectedItem().toString();

    if (!phys.equals("None") && !phys.contains("Item")) weaknesses.add(phys);
    if (!elem.equals("None") && !elem.contains("Item")) weaknesses.add(elem);
    if (!status.equals("None") && !status.contains("Item")) weaknesses.add(status);
    String combinedWeakness = String.join(", ", weaknesses);

    // SQL query mapping to match your structural layout
    String sql = "UPDATE bosses SET name = ?, weakness = ?, notes = ? WHERE name = ?";

    try (Connection conn = DBConnection.getConnection();
         PreparedStatement ps = conn.prepareStatement(sql)) {
        
        ps.setString(1, name);
        ps.setString(2, combinedWeakness);
        ps.setString(3, notes);
        ps.setString(4, originalBossName); // Find the original record row
        
        int modifiedRows = ps.executeUpdate();
        if (modifiedRows > 0) {
            JOptionPane.showMessageDialog(this, "Boss strategy parameters updated successfully!");
            refreshTable(); // Refresh user view grid
        } else {
            JOptionPane.showMessageDialog(this, "No boss record found matching: " + originalBossName, "Error", JOptionPane.ERROR_MESSAGE);
        }
    } catch (SQLException ex) {
        JOptionPane.showMessageDialog(this, "Database update failure: " + ex.getMessage(), "SQL Error", JOptionPane.ERROR_MESSAGE);
    }
    }//GEN-LAST:event_updateButtonActionPerformed

    private void deleteButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteButtonActionPerformed
   String name = nameField.getText().trim();

    if (name.isEmpty()) {
        JOptionPane.showMessageDialog(this, "Enter a boss name to delete.", "Input Error", JOptionPane.WARNING_MESSAGE);
        return;
    }

    int choice = JOptionPane.showConfirmDialog(this, "Permanently delete " + name + " from active listings?", "Confirm Delete", JOptionPane.YES_NO_OPTION);
    if (choice != JOptionPane.YES_OPTION) {
        return;
    }

    String sql = "DELETE FROM bosses WHERE name = ?";
    
    try (Connection conn = DBConnection.getConnection();
         PreparedStatement ps = conn.prepareStatement(sql)) {
        
        ps.setString(1, name);
        int rowsDeleted = ps.executeUpdate();
        
        if (rowsDeleted > 0) {
            JOptionPane.showMessageDialog(this, "Boss entry erased successfully.");
            refreshTable();
            // Clear out interface components cleanly
            nameField.setText("");
            notesArea.setText("");
            physicalCombo.setSelectedIndex(0);
            elementalCombo.setSelectedIndex(0);
            statusCombo.setSelectedIndex(0);
        } else {
            JOptionPane.showMessageDialog(this, "No boss found matching that name entry.", "Not Found", JOptionPane.INFORMATION_MESSAGE);
        }
    } catch (SQLException ex) {
        JOptionPane.showMessageDialog(this, "SQL execution error: " + ex.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
    }
    }//GEN-LAST:event_deleteButtonActionPerformed

