    private void searchBarFieldKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_searchBarFieldKeyReleased
String targetInput = searchBarField.getText().trim();
    if (targetInput.isEmpty()) {
        refreshTable();
        return;
    }
    DefaultTableModel gridDataView = (DefaultTableModel) bossTbl.getModel();
    gridDataView.setRowCount(0); 

    // Explicitly title columns to match your text notes change
    gridDataView.setColumnIdentifiers(new String[]{"ID", "Boss Name", "Weaknesses", "Notes"});

    String sql = "SELECT boss_id, name, recommended_damage_type, notes FROM bosses WHERE name LIKE ?";

    try (Connection conn = DBConnection.getConnection();
         PreparedStatement ps = conn.prepareStatement(sql)) {
        
        ps.setString(1, "%" + targetInput + "%");
        ResultSet set = ps.executeQuery();
        
        while (set.next()) {
            gridDataView.addRow(new Object[]{
                set.getInt("boss_id"),
                set.getString("name"),
                set.getString("weakness"),
                set.getString("notes")
            });
        }
    } catch (SQLException ex) {
        System.err.println("Live lookup interface stream failure context: " + ex.getMessage());
    }  
    }//GEN-LAST:event_searchBarFieldKeyReleased

