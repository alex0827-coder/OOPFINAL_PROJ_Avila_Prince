    private void deleteBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteBtnActionPerformed
    int selectedRow = tblHistory.getSelectedRow();
        if (selectedRow == -1) {
            JOptionPane.showMessageDialog(this, "Select a legendary chronicle entry to remove.");
            return;
        }

        // Section 7 Requirement: Delete operations must require confirmation
        int confirm = JOptionPane.showConfirmDialog(this, 
                "Are you sure you want to strike this victory from history?", 
                "Melina's Warning", JOptionPane.YES_NO_OPTION, JOptionPane.WARNING_MESSAGE);

        if (confirm == JOptionPane.YES_OPTION) {
            int conquestId = (int) tblHistory.getValueAt(selectedRow, 0);
            String sql = "DELETE FROM boss_conquests WHERE conquest_id = ?";

            try (Connection conn = DBConnection.getConnection();
                 PreparedStatement ps = conn.prepareStatement(sql)) {
                
                ps.setInt(1, conquestId);
                ps.executeUpdate();
                
                JOptionPane.showMessageDialog(this, "Chronicle rewritten. Legend forgotten.");
                refreshHistoryTable(""); // Refresh screen instantly!
                
            } catch (SQLException ex) {
                JOptionPane.showMessageDialog(this, "Failed to alter history: " + ex.getMessage());
            }
    }//GEN-LAST:event_deleteBtnActionPerformed
    }
