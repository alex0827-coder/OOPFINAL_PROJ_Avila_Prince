  private void refreshHistoryTable(String keyword) {
        DefaultTableModel model = (DefaultTableModel) tblHistory.getModel();
        model.setRowCount(0); // Clear old rows
        
        String sql = "SELECT * FROM boss_conquests WHERE character_name LIKE ? OR boss_name LIKE ? ORDER BY date_felled DESC";
        
        try (Connection conn = DBConnection.getConnection();
             PreparedStatement ps = conn.prepareStatement(sql)) {
            
            ps.setString(1, "%" + keyword + "%");
            ps.setString(2, "%" + keyword + "%");
            ResultSet rs = ps.executeQuery();
            
            while (rs.next()) {
                model.addRow(new Object[]{
                    rs.getInt("conquest_id"),
                    rs.getString("character_name"),
                    rs.getString("boss_name"),
                    rs.getTimestamp("date_felled")
                });
            }
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(this, "Failed to load Chronicles: " + ex.getMessage());
        }
    }
