    private void updateButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_updateButtonActionPerformed
String category = categoryCombo.getSelectedItem().toString();
String sql = "UPDATE arsenal SET weapon_name = ?, category = ?, weight = ?, damage = ?, damage_type = ?, scaling = ? WHERE weapon_name = ?";

try (Connection conn = DBConnection.getConnection();
     PreparedStatement ps = conn.prepareStatement(sql)) {
    
    ps.setString(1, weaponNameField.getText().trim());
    ps.setString(2, category);
    ps.setDouble(3, Double.parseDouble(weightField.getText().trim()));
    ps.setInt(4, Integer.parseInt(damageField.getText().trim()));
    ps.setString(5, damagetypeCombo.getSelectedItem().toString()); // FIXED: lowercase 't'
    ps.setString(6, scalingField.getText().trim());
    ps.setString(7, originalWeaponName);

    if (ps.executeUpdate() > 0) {
        JOptionPane.showMessageDialog(this, category + " inventory updated.");
        refreshTable();
    }
} catch (Exception e) {
    JOptionPane.showMessageDialog(this, "Error updating properties: " + e.getMessage());
}
    }//GEN-LAST:event_updateButtonActionPerformed

