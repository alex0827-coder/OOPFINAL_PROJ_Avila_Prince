    private void deleteButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteButtonActionPerformed
    // 1. Get the highlighted row index from the table
    int selectedRowIndex = arsenalTbl.getSelectedRow();
    
    // 2. Validation: Make sure they actually clicked a row first
    if (selectedRowIndex == -1) {
        JOptionPane.showMessageDialog(this, "Please select an item from the table grid to delete.", "Selection Missing", JOptionPane.WARNING_MESSAGE);
        return;
    }
    
    // 3. Pull the Unique ID (Column 0) and Item Name (Column 1) directly from the row
    String itemId = arsenalTbl.getValueAt(selectedRowIndex, 0).toString();
    String itemName = arsenalTbl.getValueAt(selectedRowIndex, 1).toString();
    
    // 4. Prompt a safety confirmation window
    int verify = JOptionPane.showConfirmDialog(this, "Are you sure you want to permanently delete '" + itemName + "'?", "Confirm Deletion", JOptionPane.YES_NO_OPTION);
    if (verify != JOptionPane.YES_OPTION) {
        return; // Stop execution if they click 'No'
    }
    
    // 5. Execute the SQL target match using the Primary Key ID (safest approach)
    String sql = "DELETE FROM arsenal WHERE item_id = ?";

    try (Connection conn = DBConnection.getConnection();
         PreparedStatement ps = conn.prepareStatement(sql)) {
        
        ps.setInt(1, Integer.parseInt(itemId)); // Converts the string ID to a database integer
        
        int rowsDeleted = ps.executeUpdate();
        if (rowsDeleted > 0) {
            JOptionPane.showMessageDialog(this, itemName + " successfully purged from your inventory.");
            
            // 6. Instantly clear input UI boxes to keep things clean
            weaponNameField.setText("");
            weightField.setText("");
            damageField.setText("");
            scalingField.setText("");
            categoryCombo.setSelectedIndex(0);
            originalWeaponName = ""; // Reset tracker variable
            
            // 7. Refresh the UI table layout
            refreshTable();
        }
    } catch (SQLException ex) {
        JOptionPane.showMessageDialog(this, "Database Execution Failure: " + ex.getMessage(), "SQL Error", JOptionPane.ERROR_MESSAGE);
    }
    }//GEN-LAST:event_deleteButtonActionPerformed

