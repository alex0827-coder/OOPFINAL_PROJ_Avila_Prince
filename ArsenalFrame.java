    private void addButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addButtonActionPerformed
String name = weaponNameField.getText().trim();
String category = categoryCombo.getSelectedItem().toString();
String weightStr = weightField.getText().trim();
String attackStr = damageField.getText().trim();
String damageType = damagetypeCombo.getSelectedItem().toString(); // FIXED: lowercase 't'
String scaling = scalingField.getText().trim();

if (name.isEmpty()) {
    JOptionPane.showMessageDialog(this, "Item name is required!", "Input Error", JOptionPane.WARNING_MESSAGE);
    return;
}

String sql = "INSERT INTO arsenal (weapon_name, category, weight, damage, damage_type, scaling) VALUES (?, ?, ?, ?, ?, ?)";

try (Connection conn = DBConnection.getConnection();
     PreparedStatement ps = conn.prepareStatement(sql)) {
    
    ps.setString(1, name);
    ps.setString(2, category);
    
    double weight = weightStr.isEmpty() ? 0.0 : Double.parseDouble(weightStr);
    int attack = attackStr.isEmpty() ? 0 : Integer.parseInt(attackStr);
    
    ps.setDouble(3, weight);
    ps.setInt(4, attack);
    ps.setString(5, damageType); 
    ps.setString(6, scaling);
    
    if (ps.executeUpdate() > 0) {
        JOptionPane.showMessageDialog(this, name + " added to your arsenal!");
        refreshTable();
    }
} catch (Exception ex) {
    JOptionPane.showMessageDialog(this, "Error saving entry: " + ex.getMessage());
}
    }//GEN-LAST:event_addButtonActionPerformed

